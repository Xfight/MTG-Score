    }
    
    // Configure the cell...
    PairUrlLanguage *pul = (PairUrlLanguage *)[expansion.arrayUrlLanguage objectAtIndex:indexPath.row];
    cell.textLabel.text = pul.language;
    cell.accessoryType = UITableViewCellAccessoryDisclosureIndicator;
    
    return cell;
}

